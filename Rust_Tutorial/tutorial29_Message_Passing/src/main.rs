
use std::sync::mpsc;
use std::thread;
use std::time::Duration;
fn main() {
    println!("Hello, world!");

    //Example producer and consumer
    let (tx, rx) = mpsc::channel();
    thread::spawn(move ||{
        let msg = String::from("hi");
        tx.send(msg).unwrap();
        //println!("{}", msg);
    });
    let recieved = rx.recv().unwrap();
    println!("Got: {}", recieved);

    //Looping multiple messages 
    let (tx, rx) = mpsc::channel();
    thread::spawn(move || {
        let vals = vec![
            String::from("hi"),
            String::from("from"),
            String::from("the"),
            String::from("thread"),
        ];

        for val in vals {
            tx.send(val).unwrap();
            thread::sleep(Duration::from_secs(1));
        }
    });

    for received in rx {
        println!("Got: {}", received);
    }

    //Multiple Producers and Single Consumer
    let (tx, rx) = mpsc::channel();

    let tx1 = tx.clone();
    thread::spawn(move || {
        let vals = vec![
            String::from("hi"),
            String::from("from"),
            String::from("the"),
            String::from("thread"),
        ];

        for val in vals {
            tx1.send(val).unwrap();
            thread::sleep(Duration::from_secs(1));
        }
    });

    thread::spawn(move || {
        let vals = vec![
            String::from("more"),
            String::from("messages"),
            String::from("for"),
            String::from("you"),
        ];

        for val in vals {
            tx.send(val).unwrap();
            thread::sleep(Duration::from_secs(1));
        }
    });

    for received in rx {
        println!("Got: {}", received);
    }

}
